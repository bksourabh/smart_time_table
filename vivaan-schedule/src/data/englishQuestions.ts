// Helper functions
export function getQuestionsByCategory(category: EnglishCategoryType): EnglishQuestion[] {
  return englishQuestions.filter(q => q.category === category);
}

export function getQuestionsByStrand(strand: EnglishStrandType): EnglishQuestion[] {
  return englishQuestions.filter(q => q.strand === strand);
}

export function getRandomQuestions(count: number): EnglishQuestion[] {
  const shuffled = [...englishQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getCategoriesByStrand(strand: EnglishStrandType): EnglishCategoryType[] {
  const strandCategories: Record<EnglishStrandType, EnglishCategoryType[]> = {
    'reading': ['comprehension', 'inference', 'text-structure', 'literary-devices'],
    'writing': ['grammar', 'punctuation', 'sentence-structure', 'text-types'],
    'spelling-vocabulary': ['spelling', 'homophones', 'vocabulary', 'word-families'],
  };
  return strandCategories[strand];
}
